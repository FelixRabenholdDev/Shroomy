/**
 * Tracks the state (pressed or not) of specific keyboard keys.
 *
 * @class Keyboard
 */
class Keyboard {
  /** True if the left arrow key is pressed */
  /** @type {boolean} */
  LEFT = false;

  /** True if the right arrow key is pressed */
  /** @type {boolean} */
  RIGHT = false;

  /** True if the up arrow key is pressed */
  /** @type {boolean} */
  UP = false;

  /** True if the down arrow key is pressed */
  /** @type {boolean} */
  DOWN = false;

  /** True if the spacebar is pressed */
  /** @type {boolean} */
  SPACE = false;

  /** True if the "D" key is pressed */
  /** @type {boolean} */
  D = false;
}
